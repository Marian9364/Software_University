# turning a number from 1 to 7 into a day of the week

week_in_nums = int(input())

if week_in_nums == 1:
    print("Monday")
elif week_in_nums == 2:
    print("Tuesday")
elif week_in_nums == 3:
    print("Wednesday")
elif week_in_nums == 4:
    print("Thursday")
elif week_in_nums == 5:
    print("Friday")
elif week_in_nums == 6:
    print("Saturday")
elif week_in_nums == 7:
    print("Sunday")
else:
    print("Error")
